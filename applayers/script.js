function ativar(){
			if (document.getElementById("l").style.display=='block') {
				document.getElementById("l").style.display='none';
			}else{
				document.getElementById("l").style.display='block';
			}
		}

function linkar(){
	if (document.getElementById("l").style.display=='block') {
		document.getElementById("l").style.display='none';
		}
}
